#include "libft.h"

int	ft_isalnum(int a)
{
	if ((a >= 48 && a <= 57) || (a >= 'A' && a <= 'Z') \
			|| (a >= 'a' && a <= 'z'))
		return (1);
	else
		return (0);
}
